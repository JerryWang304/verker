#!/usr/bin/env perl

print '"';
foreach(split /,/, <DATA>) {
   chomp;
   my $code;
   if (/^0$/) {
      $code = '\x00';
   } elsif (index($_, '|') != -1) {
      my @codes = map hex, split /\|/;
      my $res = 0;
      map { $res |= $_ } @codes;
      $code = sprintf '\x%x', $res;
   } else {
      $code = s!0x!\\x!r;
   }
   print $code;
}
print "\"\n";


